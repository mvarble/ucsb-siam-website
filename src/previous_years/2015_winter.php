					<p><b>Title: Smoothing Effects for Linear PDE</b></p>
					<p>An evolution equation is a partial differential equation (PDE) whose solution extends the initial configuration u_0 of a physical system to a function u(t) of time. Often, mathematical properties of u_0 persist in the solution as time progresses. There can even be an improvement; if u_0 is a twice differentiable function, the solution may possess three or more derivatives. This talk investigates the so-called smoothing effects for two linear PDE.</p>
